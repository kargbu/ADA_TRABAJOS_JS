// Ciclo for se utiliza para repetir un bloque de código un número específico de veces. 
for (j = 1; j < 4; j++) {
    console.log(j);
}

// Ciclo con while, se ejecuta mientras una condición es verdadera
let i = 0
while (i < 5) {
    console.log(i);
    i++;
}

// Cuenta regresiva
for (k = 10; k > 0; k--){
    console.log(k);
}
